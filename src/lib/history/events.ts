/**
 * Get events by category
 */
export function getEventsByCategory(category: EventCategory): HistoricalEvent[] {
  return HISTORICAL_EVENTS.filter(event => event.category === category);
}

/**
 * Get events within a date range
 */
export function getEventsInRange(startYear: number, endYear: number): HistoricalEvent[] {
  return HISTORICAL_EVENTS.filter(
    event => event.year >= startYear && event.year <= endYear
  );
}

/**
 * Get events by region
 */
export function getEventsByRegion(regionCode: string): HistoricalEvent[] {
  return HISTORICAL_EVENTS.filter(
    event => event.regions?.includes(regionCode)
  );
}

